// Q3
// Display the following figure
// Rectangle of red color
// Text "This is computer graphics using C program" in red color
// Using graphics.h library

#include <graphics.h>

int main()
{
    int gd = DETECT, gm;
    initgraph(&gd, &gm, "C:\\TC\\BGI");

    setcolor(RED);
	rectangle(100, 100, 500, 400);
    outtextxy(100, 150, "This is computer graphics using C program");

    getch();
    closegraph();
    return 0;
}
